# ALTERNATE CASE (7 kyu)

# Instructions
# Write function alternateCase which switch every letter in string from upper to lower and from lower to upper. E.g: Hello World -> hELLO wORLD

# Solution
def alternate_case(s)
  s.swapcase
end


