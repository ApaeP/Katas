# Codewars - Kata Solution
# GREET ME (7 kyu)

# Instructions
# Write a method that takes one argument as name and then greets that name, capitalized and ends with an exclamation point.
# Example:
# "riley" --> "Hello Riley!"
# "JACK"  --> "Hello Jack!"

# Solution
def greet(name) "Hello #{name.capitalize}!" end


