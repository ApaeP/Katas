# Codewars - Kata Solution
# UNIQUE NUMBERS (7 kyu)

# Instructions
# You have to write a function which returns unique numbers array. You don't need to validate input arg arr. Retain the original order of the input.
# Examples:
# unique_numbers([1,1,2,2]) => [1,2]
# unique_numbers([1,2,3,1]) => [1,2,3]

# Solution
def unique_numbers(numbers_array)
  numbers_array.uniq
end


