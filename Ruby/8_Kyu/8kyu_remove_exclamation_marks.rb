# Codewars - Kata Solution
# REMOVE EXCLAMATION MARKS (8 kyu)

# Instructions
# Write function RemoveExclamationMarks which removes all exclamation marks from a given string.

# Solution
def remove_exclamation_marks(s)
  s.gsub('!', '')
end


