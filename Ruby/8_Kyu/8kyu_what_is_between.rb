# Codewars - Kata Solution
# WHAT IS BETWEEN (8 kyu)

# Instructions
# Complete the function that takes two integers (a, b, where a < b) and return an array of all integers between the input parameters, including them.
# For example:
# a = 1
# b = 4
# --> [1, 2, 3, 4]

# Solution
def between(a, b)
  (a..b).to_a
end


