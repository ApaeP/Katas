# Codewars - Kata Solution
# SIMPLE MULTIPLICATION (8 kyu)

# Instructions
# This kata is about multiplying a given number by eight if it is an even number and by nine otherwise.

# Solution
def simple_multiplication(num)
  num.even? ? num*8 : num*9
end


