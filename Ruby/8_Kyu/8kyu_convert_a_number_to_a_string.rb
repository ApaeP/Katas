# Codewars - Kata Solution
# CONVERT A NUMBER TO A STRING (8 kyu)

# Instructions
# We need a function that can transform a number into a string.
# What ways of achieving this do you know?
# Examples:
# 123 --> "123"
# 999 --> "999"

# Solution
def numberToString(n)
  n.to_s
end


