# Codewars - Kata Solution
# BEGINNER  REDUCE BUT GROW (8 kyu)

# Instructions
# Given a non-empty array of integers, return the result of multiplying the values together in order. Example:
# [1, 2, 3, 4] => 1 * 2 * 3 * 4 = 24

# Solution
def grow(x) 
  x.inject(:*)
end


