# Codewars - Kata Solution
# CONVERT BOOLEAN VALUES TO STRINGS YES OR NO (8 kyu)

# Instructions
# Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

# Solution
def bool_to_word bool
  bool ? 'Yes' : 'No'
end


